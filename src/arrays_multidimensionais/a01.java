package src.arrays_multidimensionais;

public class a01 {
    public static void main(String[] args) {
        // Arrays Multidimensionais
        int[][] ano = {{1,2,3}, {1,2,3}, {33,22,11,00}};

        for(int[] i: ano){
            for(int x: i){
                System.out.println(x);
            }
        }


    }
    
}
