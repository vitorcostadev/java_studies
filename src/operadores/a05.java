package src.operadores;

public class a05 {
    public static void main(String[] args) {
        // Atribuição 
        // += -= *= /= %=

        int x = 5, y = 10;

        System.out.println(x+=1000);
        System.out.println(y-=1);
        System.out.println(x*=2);
        System.out.println(y/=2);
        System.out.println(x%=2);
    }
}
